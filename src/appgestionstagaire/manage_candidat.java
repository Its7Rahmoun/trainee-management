    private void btnprintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnprintActionPerformed
        // TODO add your handling code here:
        boolean ispotentialcandidat = false;
        String nom = txtnom.getText();
        String email = txtemail.getText();
        String path = "C:\\Users\\User\\Documents\\NetBeansProjects\\APPGestionStagaire\\convantionsnosigne\\";
        String query = "select * from person where email='" + email + "' and nom = '" + nom + "'";

        try {
            ResultSet rs = operation.getdate(query);
            if (rs.next()) {
                ResultSet rs1 = operation.getdate("select * from candidat where id_person=" + rs.getInt("id"));
                if (rs1.next()) {
                    ispotentialcandidat = rs1.getBoolean("etat");

                    if (ispotentialcandidat) {

                        com.itextpdf.text.Document doc = new com.itextpdf.text.Document();
                        try {
                            PdfWriter.getInstance(doc, new FileOutputStream(path + "convantioN" + rs.getString("nom") + ".pdf"));
                            doc.open();
                            Paragraph catalogue = new Paragraph("                                                              convantion \n");
                            doc.add(catalogue);
                            Paragraph starline = new Paragraph("*******************************************************************************************************\n");
                            doc.add(starline);
                            Paragraph p3 = new Paragraph("\n Nom de l'entreprise: fsts \n");
                            Paragraph p33 = new Paragraph("\n Adresse de l'entreprise: boulevard Mohammed-V casa  \n");
                            Paragraph pr = new Paragraph("Mme/M. " + nom + " " + rs.getString("prenom") + " avec @mail :" + rs.getString("email") + " address : " + rs.getString("address") + " ");
                            doc.add(p3);
                            doc.add(p33);
                            doc.add(pr);
                            doc.add(starline);
                            Paragraph p4 = new Paragraph("signature du etablissement :    ");
                            doc.add(p4);
                            doc.add(starline);
                            JOptionPane.showMessageDialog(null, "pdf has been created");
                        } catch (DocumentException | FileNotFoundException e) {
                            JOptionPane.showMessageDialog(null, e);
                        }
                        doc.close();
                    } else {
                        JOptionPane.showMessageDialog(null, "this s not a potencial candidat ");
                    }

                }
            } else {
                JOptionPane.showMessageDialog(null, " candidat doesn't exist ");
            }
        } catch (SQLException ex) {
            Logger.getLogger(manage_candidat.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnprintActionPerformed

