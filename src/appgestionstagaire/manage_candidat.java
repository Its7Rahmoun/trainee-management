    public void inputverification() {
        String cemail = txtemail.getText();
        String chiefmail = txtchief.getText();
        String nom = txtnom.getText();
        String prenom = txtprenom.getText();
        String id = txtidstage.getText();
        if (!cemail.isEmpty() && cemail.matches(mailsecurity) && !chiefmail.isEmpty() && !nom.isEmpty() && !prenom.isEmpty() && !id.isEmpty()) {
            btnassign.setEnabled(true);
            btnprint.setEnabled(true);
            btncancel.setEnabled(true);
        } else {
            btnassign.setEnabled(false);
            btnprint.setEnabled(false);
            btncancel.setEnabled(false);

        }

    }

