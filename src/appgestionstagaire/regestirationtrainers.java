    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        File doc = cv.getCurrentDirectory();
        String nom = txtnom.getText();
        String prenom = txtprenom.getText();
        String add = txtadd.getText();
        String mobile = txtmobile.getText();
        String date = txtdate.getText();
        String email = txtemail.getText();
        String path = doc.getPath();
        String filename = txtfilename.getText();
        String id1 = txtid1.getText();
        String id2 = txtid2.getText();
        String id3 = txtid3.getText();
        ArrayList<Integer> stagepostuler = new ArrayList<>(3);
        if (!id1.isEmpty() ) stagepostuler.add(Integer.valueOf(id1));
        if (!id2.isEmpty()) stagepostuler.add(Integer.valueOf(id2));
        if (!id3.isEmpty()) stagepostuler.add(Integer.valueOf(id3));

        if (new File(path + "\\" + filename).exists()) {
            
            try {
                hira_date_acces.save(nom, prenom, email, mobile, path + "\\" + filename, date, add, stagepostuler);
                clear();

            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "this file doessn't exist in this directory");
        }


    }//GEN-LAST:event_btnsaveActionPerformed

