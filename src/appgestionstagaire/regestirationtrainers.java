    public String mailsecurity = "^[a-zA-Z0-9]+[@]+[a-zA-Z0-9]+[.]+[a-zA-Z0-9]+$";

    public void inputverification() {
        String email = txtemail.getText();
        String nom = txtnom.getText();
        String prenom = txtprenom.getText();
        String mobile = txtmobile.getText();
        File doccv;
        doccv = cv.getCurrentDirectory();
        String datedebut = txtdate.getText();
        String filename = txtfilename.getText();
        String add = txtadd.getText();
        String id1 = txtid1.getText();
        String id2 = txtid2.getText();
        String id3 = txtid3.getText();

        if (!email.isEmpty() && email.matches(mailsecurity) && !prenom.isEmpty() && !mobile.isEmpty() && !datedebut.isEmpty() && doccv.exists() && !add.isEmpty() && !filename.isEmpty()) {
            if (!id1.isEmpty() || !id2.isEmpty() || !id3.isEmpty()) {
                btnsave.setEnabled(true);
            }

        } else {
            btnsave.setEnabled(false);
        }

    }

